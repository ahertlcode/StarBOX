// HTTP Handler and Configuration
include '../assets/config.php';

// Social Sharing API \ Read the details of a published message
// http://docs.oneall.com/api/resources/social-sharing/read-message/

//Get the details of this message
$message_token = '881e4f95-3620-4d76-9527-545e47d898c9';

//Make Request
$oneall_curly->get (SITE_DOMAIN . "/sharing/messages/" . $message_token . ".json");
$result = $oneall_curly->get_result ();

//Success
if ($result->http_code == 200)
{
	echo "<h1>Success " . $result->http_code . "</h1>";
	echo "<pre>" . oneall_pretty_json::format_string ($result->body) . "</pre>";
}
//Error
else
{
	echo "<h1>Error " . $result->http_code . "</h1>";
	echo "<pre>" . oneall_pretty_json::format_string ($result->body) . "</pre>";
}

?>